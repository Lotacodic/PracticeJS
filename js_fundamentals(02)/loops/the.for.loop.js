// for loop has 3 parts
// 1st: initial value of a counter (we use let cos the value will later be updated)
// 2nd: A logical condition that is evaluated b4 each iteration of the loop
//      The loop will keep running while this condition is true otherwise it will stop
// 3rd: updates the counter after each iteration


for(let rep = 1; rep <= 10; rep++) {
    console.log(`Lifting weights repetition ${rep}`);
}